package tupyo.domain;

public class Tupyo {
	private int kiho;
	private int age;
	
	public Tupyo() {
		
	}
	
	public Tupyo(int kiho, int age) {
		super();
		this.kiho = kiho;
		this.age = age;
	}
	public int getKiho() {
		return kiho;
	}
	public void setKiho(int kiho) {
		this.kiho = kiho;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	

}
